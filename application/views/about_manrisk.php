          <h3>Kebijakan Manajemen Risiko Perusahaan</h3>
          <h4>PENERAPAN: </h4>
          <p>Penerapan Manajemen Risiko di PT Pusri Palembang sudah dilakukan sejak tahun 2006 sebagai bagian yang tak terpisahkan dari penerapan Tata Kelola Perusahaan dengan ditandatangani Kebijakan Manajemen Risiko PT Pusri Palembang tanggal 13 Februari 2006. Dengan berjalannya waktu, telah dilakukan penyempurnaan-penyempurnaan terhadap infrastruktur Manajemen Risiko di PT Pusri Palembang, sehingga pelaksanaan Manajemen Risiko di PT Pusri Palembang sejak tahun 2013 menerapkan Manajemen Risiko berbasis ISO 31000.

          <p> Dalam usaha mencapai visi dan misi perusahaan serta dalam menjalankan bisnis usahanya, PT Pusri Palembang menghadapi ketidakpastian, tantangan dan hambatan yang berasal dari kondisi internal maupun eksternal perusahaan. Beberapa hal yang menjadi risiko signifikan yang mempengaruhi pencapaian kinerja perusahaan antara lain kontinuitas dan tingginya harga gas bumi, turunnya harga jual komoditi pupuk urea dan amoniak, membanjirnya pupuk impor dengan harga yang lebih murah, turunnya nilai tukar rupiah terhadap US dollar, ketersediaan dana untuk modal kerja dan investasi pengembangan serta kehandalan pabrik yang dapat mengganggu operasional perusahaan. Selain itu PT Pusri Palembang juga menghadapi ketidakpastian yang semakin kompleks akibat perubahan iklim bisnis yang semakin cepat dan dinamis. Hal-hal tersebut merupakan risiko bisnis yang tidak mungkin dihindari, justru harus dikelola melalui suatu mekanisme yang dinamakan “Manajemen Risiko”.
            
          <p> Dengan penerapan Manajemen Risiko yang merupakan upaya yang terkoordinasi dan diarahkan dalam pengelolaan risiko perusahaan, maka risiko yang timbul dan berpotensi mengganggu operasional perusahaan dilakukan langkah-langkah mekanisme penanganan secara berkala berupa upaya pencegahan risiko, mitigasi risiko dan berbagi risiko. Diharapkan dengan upaya-upaya tersebut, PT Pusri Palembang dapat meminimalkan dampak negatif atau kerugian dan melihat peluang yang ada untuk dimanfaatkan sehingga memberikan keuntungan bersaing bagi perusahaan.
            
          <p>Dalam penyusunan anggaran perusahaan, PT Pusri Palembang telah menerapkan Risk Based Budgeting  dan menggunakan Quantitative Risk Analysis dalam analisa risiko serta didukung dengan adanya Sistem Informasi Manajemen Risiko (SIMRisk) yang berbasis web.
            
            Untuk mendukung keberhasilan penerapan Manajemen Risiko, PT Pusri Palembang berupaya menanamkan dan menumbuhkan budaya sadar risiko (risk culture) melalui pemahaman yang memadai tentang Manajemen Risiko secara rutin melalui Pelatihan Manajemen Risiko yang berkesinambungan.<p>
          
          

